

CREATE TABLE IF NOT EXISTS t_user (
userId INTEGER GENERATED BY DEFAULT AS IDENTITY,
userName VARCHAR(50),
password VARCHAR(50),
phone VARCHAR(50),
PRIMARY KEY (userId)
);

